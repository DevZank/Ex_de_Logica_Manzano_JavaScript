// Calcula a soma do Quadrado de Tres valores

let A               // Real
let B               // Real
let C               // Real
let quadrado        // Real

A = parseFloat(prompt("Digite o valor de A:"))
B = parseFloat(prompt("Digite o valor de B:"))
C = parseFloat(prompt("Digite o valor de C:"))

quadrado = A * A + B * B + C * C

alert(`A soma dos quadrados dos três valores é: ${quadrado}`)